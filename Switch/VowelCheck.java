// WAP to check given character is vowel or not .
class VowelCheck {
    public static void main(String[] args) {
        char c = 'm';
        switch (c) {
            case 'a' -> System.out.println("Vowel Character");
            case 'e' -> System.out.println("Vowel Character");
            case 'i' -> System.out.println("Vowel Character");
            case 'o' -> System.out.println("Vowel Character");
            case 'u' -> System.out.println("Vowel Character");
            case 'A' -> System.out.println("Vowel Character");
            case 'E' -> System.out.println("Vowel Character");
            case 'I' -> System.out.println("Vowel Character");
            case 'O' -> System.out.println("Vowel Character");
            case 'U' -> System.out.println("Vowel Character");

            default -> System.out.println("Not a Vowel Number / Consonent");
        }
    }
}
