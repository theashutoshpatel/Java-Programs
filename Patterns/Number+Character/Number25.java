class Number25 {
    public static void main(String[] args) {
        int n=1;
        char c='z';
        for(int i=1;i<=5;i++){
            System.out.print(n+ " ");
            n++;

            System.out.print(c+" ");
            c--;

            System.out.print(n+" ");
            n++;

            System.out.print(c+" ");
            c--;

            System.out.println();

        }
    }    
}
