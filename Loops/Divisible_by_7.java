// WAP TO PRINT ENTERED NUMBER IS DIVISBLE BY 7 OR NOT ?
class Divisible_by_7 {
    public static void main(String[] args) {
        int n = 21;
        if (n % 7 == 0)
            System.out.println(n + " Yes Divisible");
        else
            System.out.println(n + " No Not Divisible");
    }
}
