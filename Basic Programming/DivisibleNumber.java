// Check whether given number is divisible by 7 or not.
class DivisibleNumber {
    public static void main(String[] args) {
        int n=20;
        if(n%7==0)
            System.out.println("Yes It's Divible by 7"+n);
        else
            System.out.println("No it's Not divisible by 7"+n);
    }
}
